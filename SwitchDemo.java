/*Program on displaying entered string is vowel or consonant */
package com.apjfsa;

public class SwitchDemo {
	public static void main(String args[]) {
		char ch = 'h';
		switch (ch) {
		case 'a':
			System.out.println("vowel");
		case 'e':
			System.out.println("vowel");
		case 'i':
			System.out.println("vowel");
		case 'o':
			System.out.println("vowel");
		case 'u':
			System.out.println("vowel");
		default:
			System.out.println("consonant");
		}
	}
}
